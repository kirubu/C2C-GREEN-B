#include<stdio.h>
void main()
{
	// &,|,^,>>(divide),<<(multiply) -->perform bit wise truth table operations--->
					// convert decimal no to binary no - bitwise operation --return decimal no
	int a = 4, b= 8;
	printf("%d\n",a&b);
	printf("%d\n",a|b);
	printf("%d\n",a^b);
	printf("%d\n",8<<2);
	printf("%d\n",8>>2);
	
	
}
